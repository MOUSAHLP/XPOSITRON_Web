			function usernametyping() {
				let usernameInput = document.querySelector("input.readonly");
				let usernameInputVal = usernameInput.value;
				usernameInput.value = "";

				let typingCounter = 0;
				let interval = setInterval(() => {
					usernameInput.value += usernameInputVal[typingCounter];

